public class Percentageof5subjects {
    public static void main(String[] args) {
        //code of calculating percentage
        double mathmarks = 100; //maths marks
        double sciencemarks = 92; //science marks
        double englishmarks = 90; //english marks
        double economicsmarks = 86; //economics marks
        double physicaleducationmarks = 77; //physical education marks
        double percentage = (mathmarks + sciencemarks + englishmarks + economicsmarks + physicaleducationmarks)/5;
        System.out.println("Percentage of 5 Subjects is : " +percentage);

    }

}

